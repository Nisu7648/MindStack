class TranslationService {
  static currentLanguage = 'en';

  static setLanguage(langCode) {
    if (TRANSLATIONS[langCode]) {
      this.currentLanguage = langCode;
      return true;
    }
    return false;
  }

  static getLanguage() {
    return this.currentLanguage;
  }

  static t(key) {
    const keys = key.split('.');
    let value = TRANSLATIONS[this.currentLanguage];
    
    for (const k of keys) {
      if (value && value[k]) {
        value = value[k];
      } else {
        // Fallback to English
        value = TRANSLATIONS.en;
        for (const k2 of keys) {
          if (value && value[k2]) {
            value = value[k2];
          } else {
            return key; // Return key if not found
          }
        }
        break;
      }
    }
    
    return value;
  }

  static getAllLanguages() {
    return LANGUAGE_METADATA;
  }

  static getPopularLanguages() {
    return LANGUAGE_METADATA.filter(lang => lang.popular);
  }

  static searchLanguages(query) {
    const lowerQuery = query.toLowerCase();
    return LANGUAGE_METADATA.filter(lang => 
      lang.name.toLowerCase().includes(lowerQuery) ||
      lang.nativeName.toLowerCase().includes(lowerQuery) ||
      lang.code.toLowerCase().includes(lowerQuery)
    );
  }

  static getLanguageMetadata(langCode) {
    return LANGUAGE_METADATA.find(lang => lang.code === langCode);
  }
}

export default TranslationService;
